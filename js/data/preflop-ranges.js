// Helper to lookup a hand's GTO action for a scenario
GTO.Data.lookupPreflop = function(format, stackDepth, actionContext, positionKey, hand) {
  var fmt = this.PreflopRanges[format];
  if (!fmt) return null;

  // Find closest stack depth
  var depth = stackDepth;
  var available = Object.keys(fmt);
  if (!fmt[depth]) {
    // Find closest
    var numDepth = parseInt(depth);
    var closest = available.reduce(function(prev, curr) {
      return Math.abs(parseInt(curr) - numDepth) < Math.abs(parseInt(prev) - numDepth) ? curr : prev;
    });
    depth = closest;
  }

  var depthData = fmt[depth];
  if (!depthData || !depthData[actionContext]) return null;

  var posData = depthData[actionContext][positionKey];
  if (!posData) {
    // Try to find a fallback position key (e.g. for vs_raise, find any key containing the hero position)
    var keys = Object.keys(depthData[actionContext]);
    if (keys.length > 0 && positionKey.indexOf('_') !== -1) {
      var heroPos = positionKey.split('_').pop();
      var fallback = keys.find(function(k) { return k.indexOf('_' + heroPos) !== -1 || k.indexOf(heroPos + '_') !== -1; });
      if (fallback) posData = depthData[actionContext][fallback];
    }
    if (!posData) return null;
  }

  // Check pure raise
  if (posData.pure_raise && posData.pure_raise.indexOf(hand) !== -1) {
    if (actionContext === 'rfi') return { fold: 0, call: 0, raise: 1 };
    return { fold: 0, call: 0, raise: 1 };
  }

  // Check pure call
  if (posData.pure_call && posData.pure_call.indexOf(hand) !== -1) {
    return { fold: 0, call: 1, raise: 0 };
  }

  // Check mixed
  if (posData.mixed && posData.mixed[hand]) {
    var m = posData.mixed[hand];
    return { fold: m[0], call: m[1], raise: m[2] };
  }

  // Default: fold
  return { fold: 1, call: 0, raise: 0 };
};
